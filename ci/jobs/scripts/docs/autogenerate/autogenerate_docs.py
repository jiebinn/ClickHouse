ALL_GENERATORS = SETTINGS_GENERATORS + FUNCTION_GENERATORS


FRONTMATTER_RE = re.compile(r"\A---\n.*?\n---\n?", re.DOTALL)


def import_migrate(docs_dir):
    # The Docusaurus->Mintlify body transforms (imports @theme->/snippets,
    # HTML->MDX comments, link rewriting) are reused from the migration script
    # so generated pages match migrated ones exactly -- a single source of truth.
    sys.path.insert(0, os.path.join(docs_dir, "_migration"))
    import migrate  # noqa: E402
    return migrate


def build_file_map(migrate, slug_map):
    # docusaurus_file -> mintlify_file, plus migrate's link/import Lookups.
    lk, rows = migrate.build_lookups(Path(slug_map))
    file_map = {}
    for r in rows:
        mfile = (r.get("mintlify_file") or "").split(" | ")[0].strip()
        if r.get("status") == "matched" and mfile:
            file_map[(r.get("docusaurus_file") or "").strip()] = mfile
    return lk, file_map


def transform_body(migrate, content, src_docu, dest_path, lk):
    # Apply the same body transforms migrate.py runs on a migrated page.
    issues = []
    content, _ = migrate.transform_imports(
        content, lk, issues, source_docu_path=Path(src_docu), dest_path=Path(dest_path)
    )
    content = migrate.transform_html_comments(content)
    content = migrate.rewrite_links(content, src_docu, lk, issues, dest_path=Path(dest_path))
    return content


TITLE_RE = re.compile(r"^title:\s*(.+?)\s*$", re.MULTILINE)


def frontmatter_title(page_text):
    # The generated body carries no frontmatter -- Mintlify renders the title
    # from the dest page's frontmatter -- so read it there to feed
    # drop_redundant_h1 (which strips a duplicate leading H1 from the body).
    m = FRONTMATTER_RE.match(page_text)
    if not m:
        return None
    t = TITLE_RE.search(m.group(0))
    return t.group(1).strip().strip("'\"") if t else None


def transform_full_body(migrate, content, src_docu, dest_path, lk, title):
    # Whole-page bodies (e.g. a table engine's `Documentation` string, which is
    # the full Markdown body of its reference page) need the complete migrate
    # body pipeline -- drop the redundant H1, convert admonitions/details/tabs,
    # rewrite links and imports -- not just transform_body's narrow subset.
    # Reuse migrate_file's shared entry point so generated pages match migrated
    # ones exactly, from a single source of truth.
    return migrate.transform_body_content(
        content,
        title=title,
        lk=lk,
        issues=[],
        source_docu_path=Path(src_docu),
        dest_path=Path(dest_path),
        source_docu_file=src_docu,
        row={"docusaurus_file": src_docu},
    )


def run_sql(binary, sql_path, cwd, params=None):
    # Run the generator SQL; it writes its INTO OUTFILE markdown into `cwd`.
    cmd = [binary, "local", "--queries-file", sql_path]
    for k, v in (params or {}).items():
        cmd.append(f"--param_{k}={v}")
    subprocess.run(cmd, cwd=cwd, check=True)


def replace_between_markers(page, content):
    start = START_RE.search(page)
    end = END_RE.search(page, start.end()) if start else None
    if not start or not end:
        raise ValueError("destination is missing the AUTOGENERATED markers")
    # Keep the marker lines themselves; replace only the region between them.
    return page[:start.end()] + "\n" + content.strip("\n") + "\n" + page[end.start():]


def generate(gen, binary, docs_dir, repo_root, migrate, lk, file_map, remap):
    scratch = tempfile.mkdtemp(prefix="autogen-")
    # Stage the files the SQL reads via file() (C++ sources, etc.) into scratch.
    for staged, src in gen.get("deps", {}).items():
        shutil.copyfile(os.path.join(repo_root, src), os.path.join(scratch, staged))
    for sql in gen["sql"]:
        run_sql(binary, os.path.join(SQL_DIR, sql), scratch, gen.get("params"))
    with open(os.path.join(scratch, gen["outfile"]), encoding="utf-8") as f:
        content = f.read()
    if gen.get("skip_if_empty") and not content.strip():
        return None, None  # e.g. a page whose name is not an aggregate function

    dest_rel = file_map.get(gen["dest"]) if remap else re.sub(r"^docs/", "", gen["dest"])
    if not dest_rel:
        raise ValueError(f"no Mintlify destination for {gen['dest']}")
    dest = os.path.join(docs_dir, dest_rel)

    if gen.get("fixups"):
        content = apply_fixups(content)
    if remap:
        if gen.get("full_transform"):
            with open(dest, encoding="utf-8") as f:
                title = frontmatter_title(f.read())
            content = transform_full_body(migrate, content, gen["dest"], dest, lk, title)
        else:
            content = transform_body(migrate, content, gen["dest"], dest, lk)

    if gen["method"] == "markers":
        with open(dest, encoding="utf-8") as f:
            new = replace_between_markers(f.read(), content)
    elif gen["method"] == "body":
        # Keep the Mintlify page's frontmatter; replace the rest with the
        # generated body (its own Docusaurus frontmatter stripped).
        with open(dest, encoding="utf-8") as f:
            fm = FRONTMATTER_RE.match(f.read())
        body = FRONTMATTER_RE.sub("", content, count=1).lstrip("\n")
        new = (fm.group(0).rstrip("\n") + "\n\n" + body) if fm else body
    else:  # overwrite
        new = content
    return dest, new


def main(argv=None):
    p = argparse.ArgumentParser(description=__doc__,
                                formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--binary", default="build/programs/clickhouse")
    p.add_argument("--docs-dir", default="docs")
    p.add_argument("--slug-map",
                   help="default: <docs-dir>/_migration/slug-map.csv")
    mode = p.add_mutually_exclusive_group()
    mode.add_argument("--write", action="store_true", help="update docs in place")
    mode.add_argument("--check", action="store_true",
                      help="fail if regenerated output differs from committed docs")
    p.add_argument("--no-remap-legacy", dest="remap", action="store_false",
                   help="emit paths/links as the SQL produces them (Mintlify-native)")
    p.add_argument("--only", help="only run generators whose name contains this substring")
    args = p.parse_args(argv)

    binary = os.path.abspath(args.binary)
    docs_dir = os.path.abspath(args.docs_dir)
    repo_root = os.path.dirname(docs_dir)
    slug_map = args.slug_map or os.path.join(docs_dir, "_migration", "slug-map.csv")

    # The component-reference families (table/database engines, data types, formats,
    # table/window functions) discover their pages by iterating the slug map
    # (file_map), so build it even for a --no-remap-legacy run: those generators'
    # names are what the fast-fail check needs to tell whether the current selection
    # targets a family such a run cannot produce. The link/import remapping itself
    # only runs under --remap-legacy, so drop `migrate`/`lk` afterwards in that mode.
    migrate = import_migrate(docs_dir)
    lk, file_map = build_file_map(migrate, slug_map)
    if not args.remap:
        migrate = lk = None

    remap_only_families = [
        table_engine_generators,
        database_engine_generators,
        data_type_generators,
        format_generators,
        table_function_generators,
        window_function_generators,
    ]

    all_generators = ALL_GENERATORS + aggregate_generators(docs_dir)
    legacy_generators = []
    for builder in remap_only_families:
        legacy_generators += builder(docs_dir, file_map)

    # `--only` is a plain substring match on generator names; apply exactly that in
    # both the fast-fail check and the final selection so the two can never disagree
    # (a substring that targets a family, e.g. `--only newjson`, must be caught).
    def selected(gens):
        return [g for g in gens if not args.only or args.only in g["name"]]

    if args.remap:
        all_generators += legacy_generators
    else:
        # --no-remap-legacy is a forward-looking mode for after the generation
        # source is updated to emit Mintlify-native paths (see the module
        # docstring). Today no family can be produced in it:
        #   * the component-reference families (table/database engines, data
        #     types, formats, table/window functions) are discovered through the
        #     slug map and need its link/path remapping to even be enumerated;
        #   * settings, functions and aggregate carry hard-coded Docusaurus
        #     `dest` paths (e.g. docs/operations/settings/settings.md,
        #     docs/sql-reference/aggregate-functions/reference/<fn>.md) whose
        #     Mintlify page lives elsewhere, so the naive `re.sub(r"^docs/", "")`
        #     in generate() resolves them to files that do not exist in the
        #     Mintlify tree -- open(dest) would fail with FileNotFoundError once
        #     the SQL has run.
        # So fail fast for the whole selection (a full run, or an --only that
        # matches any generator) instead of dying mid-run, or -- for an --only
        # that matches nothing -- silently generating nothing.
        blocked = selected(all_generators) + selected(legacy_generators)
        if blocked:
            families = sorted({g["name"].split(":", 1)[0] for g in blocked})
            raise SystemExit(
                "error: --no-remap-legacy cannot generate the "
                f"{', '.join(families)} pages -- the generation source still "
                "emits Docusaurus-style paths and links, which only "
                "--remap-legacy (the default) translates to their Mintlify "
                "equivalents via the slug map (_migration/slug-map.csv). Re-run "
                "with --remap-legacy until the generation source is updated to "
                "emit Mintlify-native paths.")

    generators = selected(all_generators)
    # A selector that matches nothing is a mistake (a typo, or a family unavailable
    # in this mode); never report success while doing nothing.
    if args.only and not generators:
        raise SystemExit(f"error: --only '{args.only}' matched no generator; nothing to do.")

    drift = 0
    for gen in generators:
        dest, new = generate(gen, binary, docs_dir, repo_root, migrate, lk, file_map, args.remap)
        if new is None:  # skipped (e.g. non-aggregate page)
            continue
        rel = os.path.relpath(dest, docs_dir)
        if args.check:
            current = open(dest, encoding="utf-8").read() if os.path.isfile(dest) else ""
            if current != new:
                print(f"DRIFT: {rel} is stale")
                drift += 1
            else:
                print(f"ok: {rel}")
        elif args.write:
            with open(dest, "w", encoding="utf-8") as f:
                f.write(new)
            print(f"wrote: {rel}")
        else:
            print(f"[dry-run] would update: {rel}")
    return 1 if drift else 0


if __name__ == "__main__":
    sys.exit(main())
