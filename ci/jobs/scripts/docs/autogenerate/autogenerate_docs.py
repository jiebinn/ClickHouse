#!/usr/bin/env python3
"""
Autogenerate ClickHouse reference documentation (settings, functions, ...) from
a ClickHouse binary's introspection tables. Python port of clickhouse-docs's
`scripts/settings/autogenerate-settings.sh`, living under ci/jobs/scripts/docs
so it can run as an optional step of the docs check.

Each generator runs a `.sql` file (in ./sql) through `<binary> local`, letting
it write its `INTO OUTFILE` markdown into a scratch dir, then places that output
into the docs tree -- either overwriting a page or replacing the region between
`<!--AUTOGENERATED_START-->` / `<!--AUTOGENERATED_END-->` markers.

Legacy-link bridge: the SQL emits Docusaurus-style paths (destination files and
in-content links, e.g. `/operations/settings/settings#x`). While `--remap-legacy`
is on (the default, for the migration period), both are translated to their
Mintlify equivalents via `_migration/slug-map.csv`. Once the generation source
is updated to emit Mintlify-native paths, run with `--no-remap-legacy`. Until
then that mode cannot produce any page (every generator's destination and links
are still Docusaurus-style), so it is rejected up front rather than failing
mid-run -- see the guard in `main`.

Modes: `--write` updates the docs tree in place; `--check` regenerates to a
scratch copy and diffs against the committed docs, failing on drift (so CI can
flag reference pages that are stale relative to the binary).
"""

