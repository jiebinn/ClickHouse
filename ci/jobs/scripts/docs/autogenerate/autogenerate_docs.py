# The autogenerated region markers. Docusaurus source uses HTML comments
# (`<!--AUTOGENERATED_START-->`); migrated Mintlify pages use MDX comments
# (`{/*AUTOGENERATED_START*/}`), since MDX parses `<!-- -->` as JSX. Match either
# form and preserve whichever the page already uses.
START_RE = re.compile(r"<!--\s*AUTOGENERATED_START\s*-->|\{/\*\s*AUTOGENERATED_START\s*\*/\}")
END_RE = re.compile(r"<!--\s*AUTOGENERATED_END\s*-->|\{/\*\s*AUTOGENERATED_END\s*\*/\}")

