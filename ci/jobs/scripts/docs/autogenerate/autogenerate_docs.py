def aggregate_generators(docs_dir):
    # Aggregate functions are driven by the existing doc pages (one per function)
    # rather than a fixed list: for each page that has the AUTOGENERATED markers,
    # regenerate its body via generate-aggregate-functions.sql. Pages whose name
    # is not an aggregate function produce empty SQL output and are skipped.
    agg_dir = os.path.join(docs_dir, "reference", "functions", "aggregate-functions")
    gens = []
    for name in sorted(os.listdir(agg_dir)) if os.path.isdir(agg_dir) else []:
        if not name.endswith(".mdx") or name == "index.mdx":
            continue
        fn = name[:-len(".mdx")]
        with open(os.path.join(agg_dir, name), encoding="utf-8") as f:
            if not START_RE.search(f.read()):
                continue
        gens.append({
            "name": f"aggregate:{fn}",
            "sql": ["generate-aggregate-functions.sql"],
            "params": {"function_name": fn},
            "outfile": "temp-aggregate-function.md",
            "dest": f"docs/sql-reference/aggregate-functions/reference/{fn}.md",
            "method": "markers",
            "skip_if_empty": True,  # non-aggregate name -> empty output -> skip
        })
    return gens


# Pages whose basename does not normalise to the engine name (the SQL matches
# case/separator-insensitively, but cannot bridge a genuinely different word).
TABLE_ENGINE_PAGE_ALIASES = {"generate": "GenerateRandom"}


def table_engine_generators(docs_dir, file_map):
    # One page per table engine, discovered from the migrated docs tree the
    # same way as aggregate functions: every engine reference page carrying the
    # AUTOGENERATED markers is regenerated from system.table_engines, whose
    # `description` column holds the full Markdown body of that page. Concept
    # pages living under the same tree (replication, annindexes, ...) match no
    # engine, produce empty output, and are skipped. Discovery relies on the
    # slug-map (file_map), so this runs only with --remap-legacy (the default).
    gens = []
    for docu, mint in sorted(file_map.items()):
        if "/engines/table-engines/" not in mint or not mint.endswith(".mdx"):
            continue
        page = os.path.join(docs_dir, mint)
        if not os.path.isfile(page):
            continue
        with open(page, encoding="utf-8") as f:
            if not START_RE.search(f.read()):
                continue
        basename = os.path.basename(mint)[: -len(".mdx")]
        gens.append({
            "name": f"table-engine:{basename}",
            "sql": ["generate-table-engines.sql"],
            "params": {"engine": TABLE_ENGINE_PAGE_ALIASES.get(basename, basename)},
            "outfile": "temp-table-engine.md",
            "dest": docu,
            "method": "markers",
            "skip_if_empty": True,   # concept page -> no matching engine -> skip
            "full_transform": True,  # description is a whole Docusaurus page body
        })
    return gens


